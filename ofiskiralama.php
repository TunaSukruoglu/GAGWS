        function switchToLogin() {
            document.querySelector('#fc2').classList.remove('show', 'active');
            document.querySelector('#fc1').classList.add('show', 'active');
            document.querySelector('[data-bs-target="#fc2"]').classList.remove('active');
            document.querySelector('[data-bs-target="#fc1"]').classList.add('active');
        }

        // TOC ve okuma çubuğu
        document.addEventListener('DOMContentLoaded', function() {
            // TOC smooth scroll
            const tocLinks = document.querySelectorAll('.toc-list a');
            tocLinks.forEach(link => {
                link.addEventListener('click', function(e) {
                    e.preventDefault();
                    const targetId = this.getAttribute('href');
                    const targetElement = document.querySelector(targetId);
                    if (targetElement) {
                        targetElement.scrollIntoView({
                            behavior: 'smooth',
                            block: 'start'
                        });
                    }
                });
            });
            // TOC highlight
            const sections = document.querySelectorAll('h2[id]');
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        const id = entry.target.getAttribute('id');
                        tocLinks.forEach(link => {
                            link.classList.remove('active');
                            if (link.getAttribute('href') === `#${id}`) {
                                link.classList.add('active');
                                link.style.color = '#374151';
                                link.style.fontWeight = '600';
                            } else {
                                link.style.color = '#6b7280';
                                link.style.fontWeight = 'normal';
                            }
                        });
                    }
                });
            }, { threshold: 0.3 });
            sections.forEach(section => observer.observe(section));

            // Okuma ilerleme çubuğu
            const progressBar = document.createElement('div');
            progressBar.style.cssText = `
                position: fixed; top: 0; left: 0; width: 0%; height: 4px;
                background: linear-gradient(90deg, #374151, #1f2937);
                z-index: 9999; transition: width 0.2s ease;
            `;
            document.body.appendChild(progressBar);
            window.addEventListener('scroll', () => {
                const scrolled = (window.scrollY / (document.documentElement.scrollHeight - window.innerHeight)) * 100;
                progressBar.style.width = Math.min(scrolled, 100) + '%';
            });
        });

        // Preloader güvenli şekilde kapanır, hata olursa konsolda uyarı verir
        document.addEventListener('DOMContentLoaded', function() {
            try {
                setTimeout(function() {
                    var preloader = document.getElementById('preloader');
                    if (preloader) {
                        preloader.style.opacity = '0';
                        setTimeout(function() {
                            preloader.setAttribute('hidden', 'true');
